class Associativity
{
    public static void main(String[] args) 
    {
        int a = 6*5-34/2;
        //      30-34/2
        //      30-17
        //      13
        System.out.println(a);

        // Quick Quiz
        int x=6;
        int y=1;
        int k = x*y/2;
        //      6*1/2
        //      6/2
        //      3
        System.out.println(k);
    }
}